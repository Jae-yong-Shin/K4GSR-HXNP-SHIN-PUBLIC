"""Generate an interactive HTML survey page from a REVIEW.md file.

Parses the markdown review table and produces a standalone HTML page
where reviewers can evaluate each NLP benchmark response.

Features:
- Category-based navigation with expand/collapse
- Radio button verdicts (OK / Wrong / Missing / Unnecessary)
- Comment field per item
- Progress tracking with localStorage persistence
- JSON export of all verdicts
- Filter by category / reviewed / unreviewed / failed / flagged

Usage:
    python server/benchmark_results/generate_review_survey.py <REVIEW.md>

The input file is the *_REVIEW.md generated by generate_review_table.py.
Output: same path with _SURVEY.html extension.
"""
import re
