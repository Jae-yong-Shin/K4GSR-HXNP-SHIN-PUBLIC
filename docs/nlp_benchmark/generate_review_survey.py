def parse_review_md(md_path):
    """Parse a REVIEW.md file into structured data."""
    with open(md_path, "r", encoding="utf-8") as f:
        content = f.read()

    # Extract header metadata
    model_m = re.search(r'\*\*Model\*\*:\s*(.+)', content)
    engine_m = re.search(r'\*\*Engine\*\*:\s*(.+)', content)
    date_m = re.search(r'\*\*Date\*\*:\s*(.+)', content)
    stats_m = re.search(
        r'\*\*Total\*\*:\s*(\d+)\s*\|\s*\*\*Pass\*\*:\s*(\d+)\s*\|\s*\*\*Fail\*\*:\s*(\d+)\s*\|\s*\*\*Rate\*\*:\s*([\d.]+)%',
        content)

    meta = {
        "model": model_m.group(1).strip() if model_m else "?",
        "engine": engine_m.group(1).strip() if engine_m else "?",
        "date": date_m.group(1).strip() if date_m else "?",
        "total": int(stats_m.group(1)) if stats_m else 0,
        "pass": int(stats_m.group(2)) if stats_m else 0,
        "fail": int(stats_m.group(3)) if stats_m else 0,
        "rate": float(stats_m.group(4)) if stats_m else 0,
    }

    # Split into test blocks using ### headers
    items = []
    cat_order = []
    cat_counts = {}

    # Find category headers: ## category_name (N tests)
    current_cat = "unknown"

    # Split by ### to get individual test blocks
    blocks = re.split(r'\n### ', content)

    for block in blocks[1:]:  # Skip everything before first ###
        # Parse test header: test_id [PASS] (4.1s) or test_id [**FAIL**] (4.1s)
        header_m = re.match(
            r'(\S+)\s+\[(\*\*)?(\w+)(\*\*)?\]\s+\(([\d.]+)s\)',
            block)
        if not header_m:
            continue

        test_id = header_m.group(1)
        status = header_m.group(3)  # PASS or FAIL
        elapsed = float(header_m.group(5))
        passed = (status == "PASS")

        # Find category from the preceding ## header
        # Look backward in original content for the category
        test_pos = content.find(f"### {test_id}")
        preceding = content[:test_pos]
        cat_m = list(re.finditer(r'## (\w[\w_]*)\s+\(\d+ tests?\)', preceding))
        if cat_m:
            current_cat = cat_m[-1].group(1)

        if current_cat not in cat_counts:
            cat_order.append(current_cat)
            cat_counts[current_cat] = 0
        cat_counts[current_cat] += 1

        # Extract fields
        prompt_m = re.search(r'\*\*Prompt\*\*:\s*(.+?)(?:\n\n|\n\*\*)', block, re.DOTALL)
        actions_m = re.search(r'\*\*Actions\*\*:\s*(.+?)(?:\n\n|\n\*\*)', block, re.DOTALL)
        confirm_m = re.search(r'\*\*Confirmation\*\*:\s*(.+?)(?:\n\n|\n\*\*)', block, re.DOTALL)
        explain_m = re.search(r'\*\*Explanation\*\*:\s*(.+?)(?:\n\n|\n\*\*)', block, re.DOTALL)
        errors_m = re.search(r'\*\*Errors\*\*:\s*(.+?)(?:\n\n|\n\*\*)', block, re.DOTALL)

        prompt = prompt_m.group(1).strip() if prompt_m else "(not found)"
        actions = actions_m.group(1).strip() if actions_m else "(none)"
        explanation = explain_m.group(1).strip() if explain_m else ""
        errors = errors_m.group(1).strip() if errors_m else ""

        confirmation = None
        if confirm_m:
            cv = confirm_m.group(1).strip()
            confirmation = (cv == "True")

        items.append({
            "id": test_id,
            "cat": current_cat,
            "passed": passed,
            "prompt": prompt,
            "actions": actions,
            "explanation": explanation,
            "confirmation": confirmation,
            "elapsed": elapsed,
            "errors": errors,
        })

    return meta, items, cat_order, cat_counts


