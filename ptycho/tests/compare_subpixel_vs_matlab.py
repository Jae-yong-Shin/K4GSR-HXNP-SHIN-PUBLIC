def _load_mat73(path):
    """Load MATLAB v7.3 (.mat HDF5) file, returning a flat dict of numpy arrays."""
    out = {}
    def _vis(name, obj):
        if isinstance(obj, h5py.Dataset):
            val = obj[()]
            # HDF5 stores complex as struct with 'r' and 'i' fields
            if val.dtype.names and 'r' in val.dtype.names and 'i' in val.dtype.names:
                val = val['r'] + 1j * val['i']
            # Transpose: MATLAB stores column-major, HDF5 reads row-major
            # so h5py gives (N, ...) instead of (..., N) → need to transpose
            val = np.array(val).T if val.ndim > 1 else np.array(val).flatten()
            out[name] = val
    with h5py.File(path, 'r') as f:
        f.visititems(_vis)
    return out


