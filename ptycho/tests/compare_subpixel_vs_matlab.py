def _fix_axis_order(arr):
    """Convert MATLAB complex array to numpy complex.
    HDF5/h5py sometimes returns structured array with 'real'/'imag' fields."""
    arr = np.asarray(arr)
    if arr.dtype.names and 'real' in arr.dtype.names and 'imag' in arr.dtype.names:
        return arr['real'] + 1j * arr['imag']
    return arr.astype(complex)


def _report_test(name, true_shifts, matlab_shifts, python_shifts, tol_good=0.1, tol_ok=0.5):
    """Print formatted comparison table for one test."""
    true_shifts  = np.atleast_2d(true_shifts)
    matlab_shifts = np.atleast_2d(matlab_shifts)
    python_shifts = np.atleast_2d(python_shifts)

    n = true_shifts.shape[0]
    print(f"\n{'-'*72}")
    print(f"  {name}")
    print(f"{'-'*72}")
    hdr = f"  {'k':>3}  {'true_x':>8}  {'true_y':>8}  {'mat_x':>8}  {'mat_y':>8}  "
    hdr += f"{'py_x':>8}  {'py_y':>8}  {'e_mat':>7}  {'e_py':>7}  {'d_m-p':>9}"
    print(hdr)
    print(f"  {'-'*67}")

    err_mat  = []
    err_py   = []
    err_diff = []
    all_pass = True

    for k in range(n):
        tx, ty = true_shifts[k]
        mx, my = matlab_shifts[k]
        px, py = python_shifts[k]

        e_mat = np.sqrt((mx-tx)**2 + (my-ty)**2)
        e_py  = np.sqrt((px-tx)**2 + (py-ty)**2)
        e_diff = np.sqrt((px-mx)**2 + (py-my)**2)

        err_mat.append(e_mat);  err_py.append(e_py);  err_diff.append(e_diff)

        flag = 'OK' if e_py < tol_good else ('~' if e_py < tol_ok else 'FAIL')
        if e_py >= tol_ok:
            all_pass = False

        print(f"  {k+1:>3}  {tx:>8.3f}  {ty:>8.3f}  {mx:>8.3f}  {my:>8.3f}  "
              f"{px:>8.3f}  {py:>8.3f}  {e_mat:>7.4f}  {e_py:>7.4f}  {e_diff:>9.4f}  {flag}")

    err_mat  = np.array(err_mat)
    err_py   = np.array(err_py)
    err_diff = np.array(err_diff)

    print(f"\n  MATLAB  MAE={err_mat.mean():.4f}  max={err_mat.max():.4f}")
    print(f"  Python  MAE={err_py.mean():.4f}  max={err_py.max():.4f}")
    print(f"  Diff(Py-Mat) MAE={err_diff.mean():.4f}  max={err_diff.max():.4f}")
    print(f"  Status: {'ALL PASS' if all_pass else 'SOME FAIL'}")
    return err_mat, err_py, err_diff, all_pass


def apply_fourier_shift(blob, sx, sy):
    """Apply exact sub-pixel shift via Fourier phase ramp (matches MATLAB)."""
    Ny, Nx = blob.shape
    fy = np.fft.ifftshift(np.arange(-Ny//2, Ny//2) / Ny)
    fx = np.fft.ifftshift(np.arange(-Nx//2, Nx//2) / Nx)
    FX, FY = np.meshgrid(fx, fy)
    phase_ramp = np.exp(1j * 2 * np.pi * (sx * FX + sy * FY))
    shifted = np.fft.ifft2(np.fft.fft2(blob) * phase_ramp)
    return shifted.real.astype(complex)


