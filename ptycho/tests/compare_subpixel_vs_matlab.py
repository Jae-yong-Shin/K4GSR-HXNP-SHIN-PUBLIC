    ax.scatter(t, p, c='tomato',    s=60, marker='^', label='Python', zorder=4)
    lim = max(abs(t).max(), 1.5) * 1.2
    ax.plot([-lim, lim], [-lim, lim], 'k--', lw=0.8, alpha=0.5)
    ax.set_xlabel(f'True shift ({axis_label})')
    ax.set_ylabel(f'Estimated shift ({axis_label})')
    ax.set_title(title)
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

