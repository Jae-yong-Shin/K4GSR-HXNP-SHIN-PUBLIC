def scatter_plot(ax, true_sh, mat_sh, py_sh, title, axis_label):
    t = true_sh[:, 0] if axis_label == 'x' else true_sh[:, 1]
    m = mat_sh[:, 0]  if axis_label == 'x' else mat_sh[:, 1]
    p = py_sh[:, 0]   if axis_label == 'x' else py_sh[:, 1]
